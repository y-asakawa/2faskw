    warn "LOCAL_TEST_LDAP_INSTALL_WEBAUTHN_SCHEMA is implemented only for 389ds; prepare LDAP schema manually for $LOCAL_TEST_LDAP_BACKEND"
    return 0
  fi
  say "== Configure local 389ds WebAuthn LDAP storage schema =="
  require_command_for_apply ldapmodify openldap-clients
  require_command_for_apply ldapsearch openldap-clients

  local attr_defs=(
    "101:$WEBAUTHN_LDAP_ATTR_CONTEXT"
    "102:$WEBAUTHN_LDAP_ATTR_ID"
    "103:$WEBAUTHN_LDAP_ATTR_EXPIRES"
    "104:$WEBAUTHN_LDAP_ATTR_VALUE"
    "105:$WEBAUTHN_LDAP_ATTR_VERSION"
  )
  local item oid_suffix attr_name
  for item in "${attr_defs[@]}"; do
    oid_suffix="${item%%:*}"
    attr_name="${item#*:}"
    ldap_add_schema_attribute_if_missing "$attr_name" "$oid_suffix"
  done
}

