while [[ "$#" -gt 0 ]]; do
  case "$1" in
    --apply)
      APPLY=1
      shift
      ;;
    --config)
      [[ -n "${2:-}" ]] || {
        echo "ERROR: --config requires FILE" >&2
        exit 2
      }
      LOCAL_CONFIG_FILE="${2:-}"
      shift 2
      ;;
    --savedata)
      [[ -n "${2:-}" ]] || {
        echo "ERROR: --savedata requires db or ldap" >&2
        exit 2
      }
      CLI_GRAPHICAL_SAVEDATA="${2:-}"
      shift 2
      ;;
    --allow-existing)
      ALLOW_EXISTING_INSTALL=true
      shift
      ;;
    --webauthn)
      CLI_ENABLE_WEBAUTHN=true
      shift
      ;;
    --no-webauthn)
      CLI_ENABLE_WEBAUTHN=false
      shift
      ;;
    --skip-build)
      SKIP_BUILD=1
      shift
      ;;
    --skip-restart)
      SKIP_RESTART=1
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "ERROR: unknown argument: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

