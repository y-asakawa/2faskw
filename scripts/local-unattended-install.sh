url_origin() {
  local url="$1"
  if [[ "$url" =~ ^([A-Za-z][A-Za-z0-9+.-]*)://([^/]+) ]]; then
    printf '%s://%s' "${BASH_REMATCH[1]}" "${BASH_REMATCH[2]}"
