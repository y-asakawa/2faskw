  temp="$(mktemp)"
  python3 - "$readable_providers" "$temp" "$LOCAL_TEST_SP_METADATA_PROVIDER_ID" "$LOCAL_TEST_SP_IDP_METADATA_FILE" <<'PY'
import pathlib
import re
