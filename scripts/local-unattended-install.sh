url_scheme() {
  local url="$1"
  if [[ "$url" =~ ^([A-Za-z][A-Za-z0-9+.-]*):// ]]; then
    printf '%s' "${BASH_REMATCH[1]}" | tr '[:upper:]' '[:lower:]'
  fi
}

url_host() {
  local url="$1"
  local authority host
  if [[ "$url" =~ ^[A-Za-z][A-Za-z0-9+.-]*://([^/]+) ]]; then
    authority="${BASH_REMATCH[1]##*@}"
    if [[ "$authority" == \[*\]* ]]; then
      host="${authority%%]*}"
      host="${host#[}"
    else
      host="${authority%%:*}"
    fi
    printf '%s' "$host" | tr '[:upper:]' '[:lower:]'
  fi
}

