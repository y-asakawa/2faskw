idp_plugin_installed() {
  local plugin_id="$1"
  local plugin_script="$IDP_HOME/bin/plugin.sh"
  [[ -x "$plugin_script" ]] || return 1
  capture_root env JAVA_HOME="$JAVA_HOME_VALUE" \
    PATH="$JAVA_HOME_VALUE/bin:/usr/bin:/bin:/usr/sbin:/sbin" \
    "$plugin_script" -l 2>/dev/null \
    | grep -Fq "$plugin_id"
}

idp_plugin_truststore_path() {
  local plugin_id="$1"
  printf '%s/credentials/%s/truststore.asc' "$IDP_HOME" "$plugin_id"
}

idp_plugin_truststore_has_key() {
  local truststore="$1"
  local fingerprint
  fingerprint="$(printf '%s' "$2" | tr '[:lower:]' '[:upper:]')"
  [[ -f "$truststore" ]] || return 1
  local temp
  temp="$(mktemp)"
  if ! capture_root cat "$truststore" > "$temp" 2>/dev/null; then
    rm -f "$temp"
    return 1
  fi
  gpg --show-keys --with-colons "$temp" 2>/dev/null \
    | awk -F: '$1 == "fpr" {print toupper($10)}' \
    | grep -Fxq "$fingerprint"
  local result=$?
  rm -f "$temp"
  return "$result"
}

preload_idp_plugin_truststore() {
  local plugin_id="$1"
  local label="$2"
  local fingerprint="$3"
  if [[ "$(bool_value "$IDP_PLUGIN_PRELOAD_TRUSTSTORES")" != "true" ]]; then
    return 0
  fi
  [[ -n "$fingerprint" ]] || die "$label plugin signing key fingerprint is required"
  require_command_for_apply gpg gnupg2
  local truststore
  truststore="$(idp_plugin_truststore_path "$plugin_id")"
  if [[ "$APPLY" -eq 1 ]] && idp_plugin_truststore_has_key "$truststore" "$fingerprint"; then
    say "KEEP: Shibboleth $label Plugin truststore already contains $fingerprint"
    return 0
  fi
  say "== Prepare Shibboleth $label Plugin truststore =="
  say "+ gpg --keyserver $IDP_PLUGIN_GPG_KEYSERVER --recv-keys $fingerprint"
  say "+ export signing key to $truststore"
  if [[ "$APPLY" -eq 1 ]]; then
    local gpg_home key_file
    gpg_home="$(mktemp -d)"
    chmod 0700 "$gpg_home"
    key_file="$(mktemp)"
    GNUPGHOME="$gpg_home" gpg --batch --keyserver "$IDP_PLUGIN_GPG_KEYSERVER" \
      --recv-keys "$fingerprint"
    GNUPGHOME="$gpg_home" gpg --batch --armor --export "$fingerprint" > "$key_file"
    [[ -s "$key_file" ]] || die "failed to export $label plugin signing key: $fingerprint"
    install_file "$key_file" "$truststore" 0644 root "$IDP_RUN_GROUP"
    rm -f "$key_file"
    rm -rf "$gpg_home"
  fi
}

