preflight() {
  say "== Preflight =="
  require_command python3
  require_command_for_apply openssl openssl
  if new_idp_dry_run; then
    say "INFO: $IDP_HOME is not present during dry-run; expected after Shibboleth IdP install step"
  else
    require_dir "$IDP_HOME"
    require_file "$IDP_HOME/bin/build.sh"

    if [[ ! -f "$IDP_HOME/edit-webapp/WEB-INF/web.xml" ]]; then
      require_file "$IDP_HOME/dist/webapp/WEB-INF/web.xml"
      say "INFO: edit-webapp web.xml will be initialized from dist/webapp"
    fi
  fi

  if [[ "$APPLY" -eq 1 && "${#SUDO[@]}" -gt 0 ]]; then
    sudo -n true 2>/dev/null \
      || die "passwordless sudo is required for unattended --apply"
  fi

  if [[ -n "$JAVA_HOME_VALUE" ]]; then
    if [[ ! -x "$JAVA_HOME_VALUE/bin/java" ]]; then
      if [[ "$APPLY" -eq 0 && "$(bool_value "$INSTALL_OS_PACKAGES")" == "true" ]]; then
        say "INFO: Java not found during dry-run: $JAVA_HOME_VALUE/bin/java; expected after installing package: java-21-openjdk-devel"
      else
        die "Java not found: $JAVA_HOME_VALUE/bin/java"
      fi
    fi
  else
    require_command_for_apply java java-21-openjdk-devel
  fi

  if [[ "$(bool_value "$APPLY_DB_SCHEMA")" == "true" ]] && db_runtime_required; then
    require_command_for_apply "$PSQL_BIN" postgresql18-server
  fi

  case "$GRAPHICAL_SAVEDATA" in
    db|ldap) ;;
    *) die "GRAPHICAL_SAVEDATA must be db or ldap" ;;
  esac
  bool_value "$LOCAL_TEST_DB_ENABLED" >/dev/null
  bool_value "$LOCAL_TEST_DB_START_SERVICE" >/dev/null
  bool_value "$LOCAL_TEST_DB_RECREATE" >/dev/null
  bool_value "$INSTALL_CORE_FROM_MEDIA" >/dev/null
  bool_value "$INSTALL_OS_PACKAGES" >/dev/null
  bool_value "$IDP_INSTALL_COOKIE_SECURE" >/dev/null
  bool_value "$INSTALL_LOCAL_TEST_SP" >/dev/null
  bool_value "$LOCAL_TEST_SP_REGISTER_WITH_IDP" >/dev/null
  bool_value "$LOCAL_TEST_SP_START_SERVICE" >/dev/null
  bool_value "$INSTALL_LOCAL_TEST_LDAP" >/dev/null
  bool_value "$LOCAL_TEST_LDAP_RECREATE" >/dev/null
  bool_value "$LOCAL_TEST_LDAP_INSTALL_GRAPHICAL_SCHEMA" >/dev/null
  bool_value "$LOCAL_TEST_LDAP_PROVISION_GRAPHICAL_ATTRS" >/dev/null
  bool_value "$LOCAL_TEST_LDAP_INSTALL_WEBAUTHN_SCHEMA" >/dev/null
  bool_value "$LOCAL_TEST_LDAP_CREATE_WEBAUTHN_SUBTREE" >/dev/null
  bool_value "$LDAP_STORAGE_SUBTREE_SEARCH" >/dev/null
  bool_value "$WEBAUTHN_LDAP_USER_SUBTREE_SEARCH" >/dev/null
  bool_value "$LOCAL_TEST_USER_ENABLED" >/dev/null
  local_test_https_enabled >/dev/null || true
  bool_value "$LOCAL_TEST_TLS_SELF_SIGNED" >/dev/null
  bool_value "$LOCAL_TEST_TLS_INSTALL_CA_TRUST" >/dev/null
  case "$LOCAL_TEST_LDAP_BACKEND" in
    389ds|openldap) ;;
    *) die "LOCAL_TEST_LDAP_BACKEND must be 389ds or openldap" ;;
  esac
  bool_value "$PRODUCTION_MODE" >/dev/null
  bool_value "$ENABLE_TOTP" >/dev/null
  bool_value "$ENABLE_WEBAUTHN" >/dev/null
  bool_value "$INSTALL_IDP_AUTHN_PLUGINS" >/dev/null
  bool_value "$IDP_PLUGIN_PRELOAD_TRUSTSTORES" >/dev/null
  warn_webauthn_browser_origin
  case "$(printf '%s' "$WEBAUTHN_STORAGE" | tr '[:upper:]' '[:lower:]')" in
    auto|ldap|jdbc|db|database) ;;
    *) die "WEBAUTHN_STORAGE must be auto, jdbc, or ldap" ;;
  esac
  bool_value "$ENABLE_API" >/dev/null
  bool_value "$MFA_USE_FORWARDED_FOR" >/dev/null
  bool_value "$ALLOW_EXISTING_INSTALL" >/dev/null

  if [[ "$(bool_value "$PRODUCTION_MODE")" == "true" && "$SEQUENCE_STORAGE" == "plaintext" ]]; then
    die "plaintext sequence storage is rejected in production mode"
  fi
  if [[ "$(bool_value "$PRODUCTION_MODE")" == "true"
      && "$(bool_value "$ENABLE_TOTP")" == "true"
      && "$TOTP_SEED_STORAGE" == "plaintext" ]]; then
    die "plaintext TOTP seed storage is rejected in production mode"
  fi
  if graphical_savedata_is_ldap; then
    [[ -n "$LDAP_STORAGE_URL" ]] || die "LDAP_STORAGE_URL is required when GRAPHICAL_SAVEDATA=ldap"
    [[ -n "$LDAP_STORAGE_BASE_DN" ]] || die "LDAP_STORAGE_BASE_DN is required when GRAPHICAL_SAVEDATA=ldap"
    [[ -n "$LDAP_STORAGE_USER_FILTER" ]] || die "LDAP_STORAGE_USER_FILTER is required when GRAPHICAL_SAVEDATA=ldap"
    [[ -n "$LDAP_STORAGE_BIND_DN" ]] || die "LDAP_STORAGE_BIND_DN is required when GRAPHICAL_SAVEDATA=ldap"
    if [[ -n "$LDAP_STORAGE_BIND_PASSWORD_SOURCE_FILE" ]]; then
      require_file "$LDAP_STORAGE_BIND_PASSWORD_SOURCE_FILE"
    elif [[ -f "$LDAP_STORAGE_BIND_PASSWORD_DEST" ]]; then
      :
    else
      [[ -n "$LDAP_STORAGE_BIND_PASSWORD" ]] \
        || die "LDAP_STORAGE_BIND_PASSWORD or LDAP_STORAGE_BIND_PASSWORD_SOURCE_FILE is required when GRAPHICAL_SAVEDATA=ldap"
    fi
    case "$TOTP_SEED_STORAGE" in
      auto|hash|hash-salt-pepper|hash_salt_pepper)
        if [[ "$(bool_value "$ENABLE_TOTP")" == "true" ]]; then
          die "LDAP savedata with TOTP requires TOTP_SEED_STORAGE=keyword or aes-gcm"
        fi
        ;;
    esac
  fi
  if webauthn_ldap_storage_enabled; then
    [[ -n "$WEBAUTHN_LDAP_URL" ]] || die "WEBAUTHN_LDAP_URL is required when WebAuthn LDAP storage is enabled"
    [[ -n "$WEBAUTHN_LDAP_BIND_DN" ]] || die "WEBAUTHN_LDAP_BIND_DN is required when WebAuthn LDAP storage is enabled"
    if [[ -n "$WEBAUTHN_LDAP_BIND_PASSWORD_SOURCE_FILE" ]]; then
      require_file "$WEBAUTHN_LDAP_BIND_PASSWORD_SOURCE_FILE"
    elif [[ -f "$WEBAUTHN_LDAP_BIND_PASSWORD_DEST" ]]; then
      :
    else
      [[ -n "$WEBAUTHN_LDAP_BIND_PASSWORD" ]] \
        || die "WEBAUTHN_LDAP_BIND_PASSWORD or WEBAUTHN_LDAP_BIND_PASSWORD_SOURCE_FILE is required when WebAuthn LDAP storage is enabled"
    fi
    case "$WEBAUTHN_LDAP_LAYOUT" in
      subtree)
        [[ -n "$WEBAUTHN_LDAP_BASE_DN" ]] || die "WEBAUTHN_LDAP_BASE_DN is required for WebAuthn LDAP subtree storage"
        ;;
      user-entry)
        [[ -n "$WEBAUTHN_LDAP_USER_BASE_DN" ]] || die "WEBAUTHN_LDAP_USER_BASE_DN is required for WebAuthn LDAP user-entry storage"
        [[ -n "$WEBAUTHN_LDAP_USER_FILTER" ]] || die "WEBAUTHN_LDAP_USER_FILTER is required for WebAuthn LDAP user-entry storage"
        ;;
      *) die "WEBAUTHN_LDAP_LAYOUT must be subtree or user-entry" ;;
    esac
    [[ "$WEBAUTHN_LDAP_VALUE_STORAGE" == "plaintext" ]] \
      || die "WEBAUTHN_LDAP_VALUE_STORAGE currently supports plaintext only"
    if [[ "$LOCAL_TEST_LDAP_BACKEND" != "389ds" ]]; then
      warn "WebAuthn LDAP storage auto schema/subtree provisioning is implemented only for 389ds"
    fi
  fi

  local existing_plugin
  existing_plugin="$(find "$IDP_HOME/edit-webapp/WEB-INF/lib" -maxdepth 1 -type f \
    -name '2faskw-idp-plugin-*.jar' 2>/dev/null | head -n 1 || true)"
  if [[ -n "$existing_plugin" ]]; then
    if [[ "$(bool_value "$ALLOW_EXISTING_INSTALL")" == "true" ]]; then
      warn "existing 2FAS-KW plugin detected and ALLOW_EXISTING_INSTALL=true: $existing_plugin"
    elif [[ "$APPLY" -eq 1 ]]; then
      die "existing 2FAS-KW plugin detected; use the upgrade procedure, set ALLOW_EXISTING_INSTALL=true, or pass --allow-existing"
    else
      warn "existing 2FAS-KW plugin detected; --apply will be refused unless ALLOW_EXISTING_INSTALL=true or --allow-existing is used"
    fi
  fi
}

