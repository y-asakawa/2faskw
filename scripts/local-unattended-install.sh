verify_local_test_https() {
  if ! local_test_https_enabled; then
    return 0
  fi
  say "== Verify local test HTTPS =="
  if [[ "$APPLY" -ne 1 ]]; then
    say "+ wait for ss -ltnp to show :$LOCAL_TEST_IDP_HTTPS_PORT"
    say "+ curl -k -I https://127.0.0.1:$LOCAL_TEST_IDP_HTTPS_PORT/idp/status"
    return 0
  fi

  if command -v ss >/dev/null 2>&1; then
    local listener_ready=0
    for _ in $(seq 1 60); do
      if ss -ltnp 2>/dev/null | grep -Eq ":$LOCAL_TEST_IDP_HTTPS_PORT[[:space:]]"; then
        listener_ready=1
        break
      fi
      sleep 1
    done
    if [[ "$listener_ready" -ne 1 ]]; then
      run_root systemctl status "$JETTY_SERVICE" -l --no-pager || true
      run_root journalctl -u "$JETTY_SERVICE" -n 120 --no-pager || true
      die "Jetty is not listening on HTTPS port $LOCAL_TEST_IDP_HTTPS_PORT"
    fi
    say "OK: Jetty is listening on HTTPS port $LOCAL_TEST_IDP_HTTPS_PORT"
  else
    warn "ss was not found; skipping HTTPS listener check"
  fi

  if command -v curl >/dev/null 2>&1; then
    local status_url="https://127.0.0.1:$LOCAL_TEST_IDP_HTTPS_PORT/idp/status"
    local status_ready=0
    for _ in $(seq 1 60); do
      if curl -k -fsSI "$status_url" >/dev/null 2>&1; then
        status_ready=1
        break
      fi
      sleep 1
    done
    if [[ "$status_ready" -ne 1 ]]; then
      run_root journalctl -u "$JETTY_SERVICE" -n 120 --no-pager || true
      die "HTTPS IdP status endpoint is not reachable: $status_url"
    fi
    say "OK: HTTPS IdP status endpoint is reachable"
  else
    warn "curl was not found; skipping HTTPS status check"
  fi
}

