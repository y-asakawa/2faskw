configure_webauthn_access_control() {
  if [[ "$(bool_value "$ENABLE_WEBAUTHN")" != "true" ]]; then
    return 0
  fi
  say "== Configure WebAuthn registration access control =="
  local access_file="$IDP_HOME/conf/access-control.xml"
  local example_file="$PACKAGE_DIR/examples/access-control.xml"
  if new_idp_dry_run; then
    say "+ add AccessByCurrentUser policy to $access_file"
    say "INFO: access-control.xml dry-run is deferred because $IDP_HOME is created only by --apply"
    return 0
  fi
  if [[ ! -f "$access_file" ]]; then
    install_file "$example_file" "$access_file" 0640 root "$IDP_RUN_GROUP"
    return 0
  fi
  if capture_root grep -q 'key="AccessByCurrentUser"' "$access_file"; then
    say "KEEP: AccessByCurrentUser policy already exists in $access_file"
    return 0
  fi

  local readable_access access_copy temp
  readable_access="$access_file"
  access_copy=""
  if [[ ! -r "$access_file" ]]; then
    access_copy="$(mktemp)"
    capture_root cat "$access_file" > "$access_copy"
    readable_access="$access_copy"
  fi
  temp="$(mktemp)"
  python3 - "$readable_access" "$temp" <<'PY'
import pathlib
import sys

source = pathlib.Path(sys.argv[1])
target = pathlib.Path(sys.argv[2])
text = source.read_text(encoding="utf-8")
entry = """
        <entry key="AccessByCurrentUser">
            <bean parent="shibboleth.PredicateAccessControl">
                <constructor-arg>
                    <bean id="AccessByCurrentUserPredicate"
                          class="net.shibboleth.idp.plugin.authn.webauthn.admin.impl.AllowCurrentUserAccessPredicate" />
                </constructor-arg>
            </bean>
        </entry>
"""
if 'key="AccessByCurrentUser"' in text:
    target.write_text(text, encoding="utf-8")
    raise SystemExit(0)
marker = "</util:map>"
index = text.find(marker)
if index < 0:
    raise SystemExit("access-control.xml does not contain </util:map>")
target.write_text(text[:index].rstrip() + "\n" + entry + "\n" + text[index:], encoding="utf-8")
PY
  install_file "$temp" "$access_file" 0640 root "$IDP_RUN_GROUP"
  rm -f "$temp"
  if [[ -n "$access_copy" ]]; then
    rm -f "$access_copy"
  fi
}

