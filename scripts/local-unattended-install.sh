configure_local_http_web_xml() {
  if [[ "$(bool_value "$INSTALL_CORE_FROM_MEDIA")" != "true"
      || "$(bool_value "$IDP_INSTALL_COOKIE_SECURE")" == "true" ]]; then
    return 0
  fi
  say "== Configure web.xml local HTTP session cookie =="
  local web_xml="$IDP_HOME/edit-webapp/WEB-INF/web.xml"
  initialize_web_xml
  if [[ "$APPLY" -ne 1 && ! -f "$web_xml" ]]; then
    say "+ patch $web_xml for local HTTP test session cookie"
    say "INFO: web.xml local HTTP cookie dry-run is deferred because edit-webapp/WEB-INF/web.xml does not exist until --apply initializes it"
    return 0
  fi
  require_file "$web_xml"
  local temp readable_web_xml web_xml_copy
  readable_web_xml="$web_xml"
  web_xml_copy=""
  if [[ ! -r "$web_xml" ]]; then
    web_xml_copy="$(mktemp)"
    capture_root cat "$web_xml" > "$web_xml_copy"
    readable_web_xml="$web_xml_copy"
  fi
  temp="$(mktemp)"
  python3 - "$readable_web_xml" "$temp" <<'PY'
import pathlib
import re
import sys

source = pathlib.Path(sys.argv[1])
target = pathlib.Path(sys.argv[2])
text = source.read_text(encoding="utf-8")

def patch_cookie(match):
    session = match.group(0)
    cookie_match = re.search(r"<cookie-config\b[^>]*>.*?</cookie-config>", session, re.DOTALL)
    if not cookie_match:
        return session
    cookie = cookie_match.group(0)
    active = re.sub(r"<!--.*?-->", "", cookie, flags=re.DOTALL)
    if re.search(r"<name>\s*[^<]+\s*</name>", active, flags=re.IGNORECASE):
        cookie = re.sub(r"<name>\s*[^<]+\s*</name>", "<name>JSESSIONID</name>", cookie, count=1, flags=re.IGNORECASE)
    else:
        cookie = cookie.replace("<cookie-config>", "<cookie-config>\n            <name>JSESSIONID</name>", 1)
    active = re.sub(r"<!--.*?-->", "", cookie, flags=re.DOTALL)
    if re.search(r"<secure>\s*(true|false)\s*</secure>", active, flags=re.IGNORECASE):
        cookie = re.sub(r"<secure>\s*(true|false)\s*</secure>", "<secure>false</secure>", cookie, count=1, flags=re.IGNORECASE)
    else:
        cookie = cookie.replace("</cookie-config>", "            <secure>false</secure>\n        </cookie-config>", 1)
    return session[:cookie_match.start()] + cookie + session[cookie_match.end():]

patched = re.sub(r"<session-config\b[^>]*>.*?</session-config>", patch_cookie, text, count=1, flags=re.DOTALL)
target.write_text(patched, encoding="utf-8")
PY
  install_file "$temp" "$web_xml" 0644 root root
  rm -f "$temp"
  if [[ -n "$web_xml_copy" ]]; then
    rm -f "$web_xml_copy"
  fi
}

