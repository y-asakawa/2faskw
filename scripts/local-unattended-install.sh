# ==============================================================================
# 2FAS-KW local test installer
#
# This file is intentionally local-only. Edit the configuration block below,
# run without --apply for a dry-run, then run with --apply.
#
# Recommended preinstalled OS packages for reliable dry-runs:
#   dnf install -y java-21-openjdk-devel maven tar unzip curl openssl which gnupg2 \
#     postgresql18-server postgresql18-contrib httpd php php-cli php-fpm \
#     php-mbstring php-xml php-intl php-ldap php-opcache php-process \
#     389-ds-base openldap-clients policycoreutils-python-utils
#
# With INSTALL_OS_PACKAGES=true, --apply installs the packages above. Dry-run
# avoids stopping on commands that are only needed after --apply, but package
# build/ZIP extraction still needs maven/unzip unless PACKAGE_DIR points to an
# already extracted package. Preinstalling the packages above gives the most
# accurate dry-run.
#
# If this script is copied outside the source repository, place
# 2faskw-idp-plugin-*.zip under INSTALL_MEDIA_DIR, the script directory, or the
# current working directory, or set PACKAGE_SOURCE_MODE=directory and PACKAGE_DIR.
#
# Purpose:
#   - Release / version-up validation only
#   - Verify that a new install path and an upgrade-like re-run path complete
#   - Never use this script as a production installer
#
# Post-test settings to replace before any real environment use:
#   - LDAP: replace LOCAL_TEST_LDAP_* / ldap.properties with the real LDAP URL,
#     baseDN, bindDN, and credential. The default local LDAP is only for tests.
#   - DB: replace DB_URL, DB_USER, DB_PASSWORD/DB_PASSWORD_SOURCE_FILE, pool
#     sizing, TLS settings, backup, monitoring, and HA/VIP design.
#   - IdP identity: replace IDP_INSTALL_HOSTNAME, IDP_INSTALL_ENTITY_ID,
#     IDP_INSTALL_SCOPE, and idp-metadata.xml with the real FQDN/entityID/scope.
#   - TLS: configure the production Jetty HTTPS connector and real certificates.
#   - SP: replace the generated SimpleSAMLphp test SP with production SP metadata
#     and attribute release policy.
#   - Secrets: rotate sequence/TOTP/API/DB secrets generated by this test script.
#   - MFA methods: install/enable production TOTP/WebAuthn dependencies as needed.
#   - Operations: configure firewall, logrotate, monitoring, backup, and restart
#     policy outside this local test helper.
#
# Scope:
#   - Optional local test Jetty / Shibboleth IdP install from ~/install-media
#   - 2FAS-KW package build or package ZIP extraction
#   - package verification, plugin files, properties, secrets, web.xml
#   - optional local LDAP test directory on 127.0.0.1
#   - local test PostgreSQL DB/role/schema
#   - optional local SimpleSAMLphp test SP on http://<test-host>:8081
#   - WAR rebuild, optional Jetty restart
#
# Not automated because these are environment-specific:
#   - production OS hardening, production TLS/FQDN, and load balancer setup
#   - production LDAP bind/search settings
#   - production SP metadata and attribute release policy
#   - Jetty HTTPS connector wiring and load balancer configuration
# ==============================================================================

