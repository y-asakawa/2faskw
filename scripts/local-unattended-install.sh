install_plugin_files() {
  say "== Install plugin package files =="
  local args=(--idp-home "$IDP_HOME" --package-dir "$PACKAGE_DIR" --skip-package-check --apply)
  if [[ "$APPLY" -eq 1 ]]; then
    "$PACKAGE_DIR/bin/graphicalmatrix-plugin-config.sh" "${args[@]}"
  elif new_idp_dry_run; then
    say "+ $PACKAGE_DIR/bin/graphicalmatrix-plugin-config.sh --idp-home $IDP_HOME --package-dir $PACKAGE_DIR --skip-package-check"
    say "INFO: plugin package installer dry-run is deferred because $IDP_HOME is created only by --apply"
  else
    "$PACKAGE_DIR/bin/graphicalmatrix-plugin-config.sh" \
      --idp-home "$IDP_HOME" --package-dir "$PACKAGE_DIR" --skip-package-check
  fi

  # The package installer intentionally installs runtime/admin files only.
  # Install operational helper scripts explicitly for a complete new setup.
  for helper in graphicalmatrix-plugin-check.sh graphicalmatrix-plugin-config.sh \
      graphicalmatrix-plugin-webxml.sh graphicalmatrix-plugin-uninstall.sh; do
    install_file "$PACKAGE_DIR/bin/$helper" "$IDP_HOME/bin/$helper" 0755 root "$IDP_RUN_GROUP"
  done
}

configure_secrets() {
  say "== Configure secrets =="
  case "$SEQUENCE_STORAGE" in
    auto|hash|hash-salt-pepper|hash_salt_pepper)
      install_secret_value "$SEQUENCE_PEPPER_FILE" "$(random_hex 32)"
      ;;
    keyword|common-keyword|common_keyword)
      install_secret_value "$SEQUENCE_KEYWORD_FILE" "$(random_base64 48)"
      ;;
    aes|aes-gcm|aes_gcm)
      install_secret_value "$SEQUENCE_AES_KEY_FILE" "$(random_base64 32)"
      ;;
    plaintext)
      warn "sequence storage is plaintext"
      ;;
    *)
      die "unsupported SEQUENCE_STORAGE: $SEQUENCE_STORAGE"
      ;;
  esac

  local normalized_totp_storage
  normalized_totp_storage="$(printf '%s' "$TOTP_SEED_STORAGE" | tr '[:upper:]' '[:lower:]')"
  if [[ "$(bool_value "$ENABLE_TOTP")" == "true"
      || ! "$normalized_totp_storage" =~ ^(auto)?$ ]]; then
    case "$TOTP_SEED_STORAGE" in
      keyword|common-keyword|common_keyword)
        install_secret_value "$TOTP_KEYWORD_FILE" "$(random_base64 48)"
        ;;
      aes|aes-gcm|aes_gcm)
        install_secret_value "$TOTP_AES_KEY_FILE" "$(random_base64 32)"
        ;;
      plaintext)
        warn "TOTP seed storage is plaintext"
        ;;
      auto)
        warn "TOTP seed storage auto depends on sequence storage; explicit aes-gcm is recommended"
        ;;
      *)
        die "unsupported TOTP_SEED_STORAGE: $TOTP_SEED_STORAGE"
        ;;
    esac
  fi

  if db_runtime_required; then
    local db_password_value="${GRAPHICALMATRIX_DB_PASSWORD:-$DB_PASSWORD}"
    if [[ -n "$DB_PASSWORD_SOURCE_FILE" ]]; then
      require_file "$DB_PASSWORD_SOURCE_FILE"
      db_password_value="$(tr -d '\r\n' < "$DB_PASSWORD_SOURCE_FILE")"
    fi
    [[ -n "$db_password_value" ]] \
      || die "set DB_PASSWORD, DB_PASSWORD_SOURCE_FILE, or GRAPHICALMATRIX_DB_PASSWORD"
    install_secret_value "$DB_PASSWORD_DEST" "$db_password_value"
  fi

  if graphical_savedata_is_ldap; then
    install_secret_value "$LDAP_STORAGE_BIND_PASSWORD_DEST" "$(ldap_storage_bind_password)"
  fi
  if webauthn_ldap_storage_enabled \
      && { ! graphical_savedata_is_ldap || [[ "$WEBAUTHN_LDAP_BIND_PASSWORD_DEST" != "$LDAP_STORAGE_BIND_PASSWORD_DEST" ]]; }; then
    install_secret_value "$WEBAUTHN_LDAP_BIND_PASSWORD_DEST" "$(webauthn_ldap_bind_password)"
  fi

  if [[ "$(bool_value "$ENABLE_API")" == "true" ]]; then
    install_secret_value "$API_TOKEN_FILE" "$(random_base64 48)"
  fi
}

