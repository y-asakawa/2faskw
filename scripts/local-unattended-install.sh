  say "+ sudo env JAVA_HOME=$JAVA_HOME_VALUE PATH=$JAVA_HOME_VALUE/bin:/usr/bin:/bin /opt/src/$version_dir/bin/install.sh --noPrompt --propertyFile <local-test-properties> --targetDir $IDP_HOME --hostName $IDP_INSTALL_HOSTNAME --entityID $IDP_INSTALL_ENTITY_ID --keystorePassword *** --sealerPassword ***"
