install_tls_files() {
  if ! local_test_https_enabled && [[ "$INSTALL_TLS_FILES" -ne 1 ]]; then
    return 0
  fi
  say "== Configure local test TLS / HTTPS =="
  require_command_for_apply keytool java-21-openjdk-devel
  require_command_for_apply openssl openssl

  local keystore_password
  if [[ "$(bool_value "$LOCAL_TEST_TLS_SELF_SIGNED")" == "true" ]]; then
    install_secret_value "$LOCAL_TEST_TLS_KEYSTORE_PASSWORD_DEST" "$(random_hex 24)"
    if [[ "$APPLY" -eq 1 ]]; then
      keystore_password="$(capture_root cat "$LOCAL_TEST_TLS_KEYSTORE_PASSWORD_DEST" | tr -d '\r\n')"
    else
      keystore_password="DRY_RUN_TLS_KEYSTORE_PASSWORD"
    fi

    if [[ -f "$LOCAL_TEST_TLS_KEYSTORE_DEST" && -f "$LOCAL_TEST_TLS_CA_CERT_DEST" ]]; then
      say "KEEP: existing local test TLS keystore and CA: $LOCAL_TEST_TLS_KEYSTORE_DEST"
    else
      if [[ -f "$LOCAL_TEST_TLS_KEYSTORE_DEST" && ! -f "$LOCAL_TEST_TLS_CA_CERT_DEST" ]]; then
        warn "existing local test TLS keystore has no local CA certificate; regenerating CA-signed test certificate"
      fi
      local temp_dir temp_keystore temp_cert temp_ca_key temp_ca_cert temp_csr temp_ext san openssl_san dname
      temp_dir="$(mktemp -d)"
      temp_keystore="$temp_dir/2faskw-localtest.p12"
      temp_cert="$temp_dir/idp.crt"
      temp_ca_key="$temp_dir/2faskw-localtest-ca.key"
      temp_ca_cert="$temp_dir/2faskw-localtest-ca.crt"
      temp_csr="$temp_dir/idp.csr"
      temp_ext="$temp_dir/idp.ext"
      san="$(local_test_tls_san)"
      openssl_san="$(printf '%s' "$san" | sed 's/dns:/DNS:/g; s/ip:/IP:/g')"
      dname="CN=$IDP_INSTALL_HOSTNAME, OU=Local Test, O=2FAS-KW, L=Local, ST=Local, C=JP"
      say "+ create local test CA and issue IdP server certificate with SAN=$openssl_san"
      if [[ "$APPLY" -eq 1 ]]; then
        openssl req -x509 -newkey rsa:3072 -nodes \
          -days "$LOCAL_TEST_TLS_DAYS" \
          -subj "/CN=2FAS-KW Local Test CA/O=2FAS-KW/OU=Local Test/C=JP" \
          -addext "basicConstraints=critical,CA:TRUE,pathlen:0" \
          -addext "keyUsage=critical,keyCertSign,cRLSign" \
          -keyout "$temp_ca_key" \
          -out "$temp_ca_cert" >/dev/null 2>&1
        keytool -genkeypair \
          -storetype PKCS12 \
          -keystore "$temp_keystore" \
          -storepass "$keystore_password" \
          -keypass "$keystore_password" \
          -alias "$LOCAL_TEST_TLS_KEYSTORE_ALIAS" \
          -keyalg RSA \
          -keysize 3072 \
          -validity "$LOCAL_TEST_TLS_DAYS" \
          -dname "$dname" \
          -ext "SAN=$san" \
          -noprompt >/dev/null
        keytool -certreq \
          -keystore "$temp_keystore" \
          -storepass "$keystore_password" \
          -alias "$LOCAL_TEST_TLS_KEYSTORE_ALIAS" \
          -file "$temp_csr" >/dev/null
        cat > "$temp_ext" <<EOF
basicConstraints=critical,CA:FALSE
keyUsage=critical,digitalSignature,keyEncipherment
extendedKeyUsage=serverAuth
subjectAltName=$openssl_san
EOF
        openssl x509 -req \
          -in "$temp_csr" \
          -CA "$temp_ca_cert" \
          -CAkey "$temp_ca_key" \
          -CAcreateserial \
          -days "$LOCAL_TEST_TLS_DAYS" \
          -sha256 \
          -extfile "$temp_ext" \
          -out "$temp_cert" >/dev/null 2>&1
        keytool -importcert \
          -keystore "$temp_keystore" \
          -storepass "$keystore_password" \
          -alias 2faskw-localtest-ca \
          -file "$temp_ca_cert" \
          -noprompt >/dev/null
        keytool -importcert \
          -keystore "$temp_keystore" \
          -storepass "$keystore_password" \
          -alias "$LOCAL_TEST_TLS_KEYSTORE_ALIAS" \
          -file "$temp_cert" \
          -noprompt >/dev/null
        install_file "$temp_keystore" "$LOCAL_TEST_TLS_KEYSTORE_DEST" 0640 root "$IDP_RUN_GROUP"
        install_file "$temp_cert" "$TLS_CERT_DEST" 0644 root "$IDP_RUN_GROUP"
        install_file "$temp_ca_cert" "$LOCAL_TEST_TLS_CA_CERT_DEST" 0644 root "$IDP_RUN_GROUP"
      fi
      rm -rf "$temp_dir"
    fi
    if [[ "$(bool_value "$LOCAL_TEST_TLS_INSTALL_CA_TRUST")" == "true" ]]; then
      say "+ trust local test self-signed IdP certificate on this OS"
      if [[ "$APPLY" -eq 1 ]]; then
        if command -v update-ca-trust >/dev/null 2>&1; then
          install_file "$LOCAL_TEST_TLS_CA_CERT_DEST" "$LOCAL_TEST_TLS_CA_TRUST_DEST" 0644 root root
          run_root update-ca-trust extract
        else
          warn "update-ca-trust was not found; import $LOCAL_TEST_TLS_CA_CERT_DEST into the browser/client trust store manually"
        fi
      fi
    fi
  else
    [[ -n "$TLS_CERT_SOURCE" && -n "$TLS_KEY_SOURCE" ]] \
      || die "TLS_CERT_SOURCE and TLS_KEY_SOURCE are required when LOCAL_TEST_TLS_SELF_SIGNED=false"
    require_file "$TLS_CERT_SOURCE"
    require_file "$TLS_KEY_SOURCE"

    if [[ "$TLS_VERIFY_CERT_KEY_MATCH" -eq 1 ]]; then
      local cert_key_hash private_key_hash
      cert_key_hash="$(openssl x509 -in "$TLS_CERT_SOURCE" -pubkey -noout \
        | openssl pkey -pubin -outform DER 2>/dev/null \
        | openssl dgst -sha256 | awk '{print $NF}')"
      private_key_hash="$(openssl pkey -in "$TLS_KEY_SOURCE" -pubout -outform DER 2>/dev/null \
        | openssl dgst -sha256 | awk '{print $NF}')"
      [[ -n "$cert_key_hash" && "$cert_key_hash" == "$private_key_hash" ]] \
        || die "TLS certificate and private key do not match"
      say "OK: TLS certificate and private key match"
    fi

    local check_seconds=$((TLS_MIN_VALID_DAYS * 86400))
    openssl x509 -in "$TLS_CERT_SOURCE" -checkend "$check_seconds" -noout >/dev/null \
      || die "TLS certificate expires within $TLS_MIN_VALID_DAYS days"
    say "OK: TLS certificate is valid for more than $TLS_MIN_VALID_DAYS days"

    install_file "$TLS_CERT_SOURCE" "$TLS_CERT_DEST" 0644 root "$IDP_RUN_GROUP"
    install_file "$TLS_KEY_SOURCE" "$TLS_KEY_DEST" 0640 root "$IDP_RUN_GROUP"
    if [[ -n "$TLS_CHAIN_SOURCE" ]]; then
      install_file "$TLS_CHAIN_SOURCE" "$TLS_CHAIN_DEST" 0644 root "$IDP_RUN_GROUP"
    fi
    die "LOCAL_TEST_TLS_SELF_SIGNED=false currently installs PEM files only; provide a Jetty keystore or use LOCAL_TEST_TLS_SELF_SIGNED=true"
  fi

  if local_test_https_enabled; then
    say "+ configure Jetty HTTPS connector on port $LOCAL_TEST_IDP_HTTPS_PORT"
    run_root rm -f "$JETTY_BASE/start.d/ssl-context.ini"
