rebase_idp_path() {
  local value="$1"
  if [[ "$IDP_HOME" != "/opt/shibboleth-idp"
      && "$value" == /opt/shibboleth-idp/* ]]; then
    printf '%s%s' "$IDP_HOME" "${value#/opt/shibboleth-idp}"
  else
    printf '%s' "$value"
