ldap_entry_exists() {
  local dn="$1"
  ldapsearch -x -H "$LOCAL_TEST_LDAP_URL" \
    -D "$LOCAL_TEST_LDAP_ROOT_DN" -w "$LOCAL_TEST_LDAP_ROOT_PASSWORD" \
    -b "$dn" -s base dn >/dev/null 2>&1
}

ldap_add_if_missing() {
  local dn="$1"
  local ldif="$2"
  say "+ ldapadd entry if missing: $dn"
  if [[ "$APPLY" -ne 1 ]]; then
    return 0
  fi
  if ldap_entry_exists "$dn"; then
    say "KEEP: existing LDAP entry: $dn"
    return 0
  fi
  printf '%s\n' "$ldif" | ldapadd -x -H "$LOCAL_TEST_LDAP_URL" \
	    -D "$LOCAL_TEST_LDAP_ROOT_DN" -w "$LOCAL_TEST_LDAP_ROOT_PASSWORD"
}

ldap_aci_exists() {
  local aci_name="$1"
  ldapsearch -x -H "$LOCAL_TEST_LDAP_URL" \
    -D "$LOCAL_TEST_LDAP_ROOT_DN" -w "$LOCAL_TEST_LDAP_ROOT_PASSWORD" \
    -b "$LOCAL_TEST_LDAP_SUFFIX" -s base aci 2>/dev/null \
    | grep -Fq "acl \"$aci_name\""
}

ldap_add_aci_if_missing() {
  local aci_name="$1"
  local aci_value="$2"
  local output_file
  say "+ ldapmodify ACI if missing: $aci_name"
  if [[ "$APPLY" -ne 1 ]]; then
    return 0
  fi
  if ldap_aci_exists "$aci_name"; then
    say "KEEP: existing LDAP ACI: $aci_name"
    return 0
  fi
  output_file="$(mktemp)"
  if ldapmodify -x -H "$LOCAL_TEST_LDAP_URL" \
    -D "$LOCAL_TEST_LDAP_ROOT_DN" -w "$LOCAL_TEST_LDAP_ROOT_PASSWORD" >"$output_file" 2>&1 <<EOF
dn: $LOCAL_TEST_LDAP_SUFFIX
changetype: modify
add: aci
aci: $aci_value
EOF
  then
    rm -f "$output_file"
    return 0
  fi
  if grep -Fq "Type or value exists" "$output_file"; then
    say "KEEP: existing LDAP ACI value: $aci_name"
    rm -f "$output_file"
    return 0
  fi
  cat "$output_file" >&2
  rm -f "$output_file"
  return 1
}

