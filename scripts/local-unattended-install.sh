jetty.sslContext.keyStorePassword=$keystore_password
jetty.sslContext.keyStoreType=PKCS12
jetty.sslContext.keyManagerPassword=$keystore_password
jetty.sslContext.trustStorePath=$LOCAL_TEST_TLS_KEYSTORE_DEST
jetty.sslContext.trustStorePassword=$keystore_password
jetty.sslContext.trustStoreType=PKCS12
jetty.sslContext.alias=$LOCAL_TEST_TLS_KEYSTORE_ALIAS
jetty.ssl.host=0.0.0.0
jetty.ssl.port=$LOCAL_TEST_IDP_HTTPS_PORT
EOF
    install_generated_file "$JETTY_BASE/start.d/https.ini" 0640 "$IDP_RUN_USER" "$IDP_RUN_GROUP" <<EOF
--module=https
EOF
    say "+ validate Jetty HTTPS configuration"
    if [[ "$APPLY" -eq 1 ]]; then
      run_as_user "$IDP_RUN_USER" \
        env JAVA_HOME="$JAVA_HOME_VALUE" PATH="$JAVA_HOME_VALUE/bin:/usr/bin:/bin" \
