selinux_http_port_allows() {
  local port="$1"
  command -v semanage >/dev/null 2>&1 || return 1
  semanage port -l 2>/dev/null \
    | awk '$1 == "http_port_t" && $2 == "tcp" {
        for (i = 3; i <= NF; i++) {
          gsub(",", "", $i)
          print $i
        }
      }' \
    | awk -v p="$port" '
        $0 == p { found = 1 }
        $0 ~ /^[0-9]+-[0-9]+$/ {
          split($0, r, "-")
          if (p >= r[1] && p <= r[2]) found = 1
        }
        END { exit found ? 0 : 1 }
      '
}

