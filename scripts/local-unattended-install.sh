configure_jetty_base_modules() {
  if [[ "$(bool_value "$INSTALL_CORE_FROM_MEDIA")" != "true" ]]; then
    return 0
  fi
  if [[ "$APPLY" -eq 1 ]]; then
    require_file "$JETTY_HOME/start.jar"
  fi
  say "== Configure Jetty base modules =="
  run_root mkdir -p "$JETTY_BASE"
  run_root chown -R "$IDP_RUN_USER:$IDP_RUN_GROUP" "$JETTY_BASE"
  if [[ -f "$JETTY_BASE/start.d/http.ini"
      && -f "$JETTY_BASE/start.d/ee10-deploy.ini"
      && -f "$JETTY_BASE/start.d/ee10-annotations.ini"
      && -f "$JETTY_BASE/start.d/ee10-jsp.ini"
      && -f "$JETTY_BASE/start.d/ee10-jstl.ini" ]]; then
    say "KEEP: Jetty base modules already configured in $JETTY_BASE/start.d"
  else
    run_as_user "$IDP_RUN_USER" \
      env JAVA_HOME="$JAVA_HOME_VALUE" PATH="$JAVA_HOME_VALUE/bin:/usr/bin:/bin" \
      "$JAVA_HOME_VALUE/bin/java" \
      -Djetty.home="$JETTY_HOME" \
      -Djetty.base="$JETTY_BASE" \
      -jar "$JETTY_HOME/start.jar" \
      --add-modules=http,ee10-deploy,ee10-annotations,ee10-jsp,ee10-jstl
  fi

  if local_test_https_enabled; then
    if [[ -f "$JETTY_BASE/start.d/ssl-context.ini" ]]; then
      say "+ remove unsupported Jetty module config: $JETTY_BASE/start.d/ssl-context.ini"
      run_root rm -f "$JETTY_BASE/start.d/ssl-context.ini"
    fi
    say "+ enable Jetty HTTPS modules in $JETTY_BASE/start.d"
    run_as_user "$IDP_RUN_USER" \
      env JAVA_HOME="$JAVA_HOME_VALUE" PATH="$JAVA_HOME_VALUE/bin:/usr/bin:/bin" \
      "$JAVA_HOME_VALUE/bin/java" \
      -Djetty.home="$JETTY_HOME" \
      -Djetty.base="$JETTY_BASE" \
      -jar "$JETTY_HOME/start.jar" \
      --add-modules=ssl,https
  fi
}

