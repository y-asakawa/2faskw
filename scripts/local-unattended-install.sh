set_properties() {
  local source="$1"
  local destination="$2"
  shift 2
  require_file "$source"
  local readable_source="$source"
  local source_copy=""
  if [[ ! -r "$source" ]]; then
    source_copy="$(mktemp)"
    capture_root cat "$source" > "$source_copy"
    readable_source="$source_copy"
  fi
  local temp
  temp="$(mktemp)"
  python3 - "$readable_source" "$temp" "$@" <<'PY'
import pathlib
import sys

source = pathlib.Path(sys.argv[1])
target = pathlib.Path(sys.argv[2])
pairs = sys.argv[3:]
if len(pairs) % 2:
    raise SystemExit("property arguments must be key/value pairs")

updates = dict(zip(pairs[0::2], pairs[1::2]))
lines = source.read_text(encoding="utf-8").splitlines()
seen = set()
out = []

for line in lines:
    stripped = line.lstrip()
    if not stripped or stripped.startswith("#") or "=" not in line:
        out.append(line)
        continue
    key = line.split("=", 1)[0].strip()
    if key in updates:
        out.append(f"{key} = {updates[key]}")
        seen.add(key)
    else:
        out.append(line)

if out and out[-1] != "":
    out.append("")
for key, value in updates.items():
    if key not in seen:
        out.append(f"{key} = {value}")

target.write_text("\n".join(out).rstrip() + "\n", encoding="utf-8")
PY
  install_file "$temp" "$destination" 0640 root "$IDP_RUN_GROUP"
  rm -f "$temp"
  if [[ -n "$source_copy" ]]; then
    rm -f "$source_copy"
  fi
}

