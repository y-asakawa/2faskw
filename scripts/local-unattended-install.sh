install_jetty_from_media() {
  if [[ "$(bool_value "$INSTALL_CORE_FROM_MEDIA")" != "true" ]]; then
    return 0
  fi
  if [[ -f "$JETTY_HOME/start.jar" || -e "$JETTY_HOME" ]]; then
    say "KEEP: existing Jetty home: $JETTY_HOME"
    return 0
  fi
  say "== Install Jetty from local media =="
  local tarball version_dir
  tarball="$JETTY_TARBALL"
  if [[ -z "$tarball" ]]; then
    tarball="$(first_media_file 'jetty-home-*.tar.gz')"
    if [[ -z "$tarball" && "$(bool_value "$DOWNLOAD_INSTALL_MEDIA")" == "true" ]]; then
      tarball="$(planned_media_file "$JETTY_DOWNLOAD_URL")"
