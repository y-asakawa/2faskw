verify_zip_signature() {
  [[ "$VERIFY_PACKAGE_SIGNATURE" -eq 1 ]] || return
  require_command gpg
  require_file "$PACKAGE_ZIP"
  require_file "$PACKAGE_SIGNATURE"
  require_file "$SIGNING_PUBLIC_KEY_FILE"
  [[ -n "$EXPECTED_SIGNING_FINGERPRINT" ]] \
    || die "EXPECTED_SIGNING_FINGERPRINT is required when signature verification is enabled"

  local gpg_home fingerprint
  gpg_home="$(mktemp -d)"
  chmod 0700 "$gpg_home"
  gpg --homedir "$gpg_home" --batch --import "$SIGNING_PUBLIC_KEY_FILE" >/dev/null 2>&1
  fingerprint="$(gpg --homedir "$gpg_home" --batch --with-colons --fingerprint \
    | awk -F: '$1 == "fpr" {print toupper($10); exit}')"
  if [[ "$fingerprint" != "$(printf '%s' "$EXPECTED_SIGNING_FINGERPRINT" | tr '[:lower:]' '[:upper:]')" ]]; then
    rm -rf "$gpg_home"
    die "signing key fingerprint mismatch: actual=$fingerprint"
  fi
  gpg --homedir "$gpg_home" --batch --verify "$PACKAGE_SIGNATURE" "$PACKAGE_ZIP"
  rm -rf "$gpg_home"
  say "OK: detached signature verified"
}

