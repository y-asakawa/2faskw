  sudo -u "$IDP_RUN_USER" "${db_cmd[@]}" require-change "$LOCAL_TEST_USER_ID" "$LOCAL_TEST_GRAPHICAL_FORCE_CHANGE"
}

local_graphical_alias_to_id() {
  local token="$1"
  local old_ifs="$IFS"
  local raw pair alias graphical
  IFS=','
  local pairs=($GRAPHICAL_ALIASES)
  IFS="$old_ifs"
  for raw in "${pairs[@]}"; do
    pair="$(trim_value "$raw")"
    [[ -n "$pair" && "$pair" == *:* ]] || continue
    alias="$(trim_value "${pair%%:*}")"
    graphical="$(trim_value "${pair#*:}")"
    if [[ "$token" == "$alias" ]]; then
      printf '%s' "$graphical"
      return 0
    fi
  done
  return 1
}

local_graphical_sequence_to_ids() {
  local sequence="$1"
  local old_ifs="$IFS"
  local out="" raw token resolved
  IFS=','
  local items=($sequence)
  IFS="$old_ifs"
  for raw in "${items[@]}"; do
    token="$(trim_value "$raw")"
    [[ -n "$token" ]] || continue
    if resolved="$(local_graphical_alias_to_id "$token")"; then
      :
    else
      resolved="$token"
    fi
    if [[ -n "$out" ]]; then
      out="$out,$resolved"
    else
      out="$resolved"
    fi
  done
  [[ -n "$out" ]] || die "local test GraphicalMatrix sequence is empty"
  printf '%s' "$out"
}

local_sequence_tool_classpath() {
  local lib_dir cp plugin_jar
  local candidates=(
    "$IDP_HOME/edit-webapp/WEB-INF/lib"
    "$IDP_HOME/lib"
    "$PACKAGE_DIR/lib"
  )
  for lib_dir in "${candidates[@]}"; do
    [[ -d "$lib_dir" ]] || continue
    plugin_jar="$(find "$lib_dir" -maxdepth 1 -type f -name '2faskw-idp-plugin-*.jar' -print 2>/dev/null | head -n 1 || true)"
    [[ -n "$plugin_jar" ]] || continue
    cp="$(find "$lib_dir" -maxdepth 1 -type f -name '*.jar' -print 2>/dev/null | sort | paste -sd ':' -)"
    [[ -n "$cp" ]] || continue
    printf '%s' "$cp"
    return 0
  done
  die "2FAS-KW plugin jar not found for local sequence encoding"
}

local_graphical_sequence_encode() {
  local sequence="$1"
  local ordered="false"
  local duplicates="false"
  [[ "$GRAPHICAL_ORDER" == "1" || "$GRAPHICAL_ORDER" == "true" || "$GRAPHICAL_ORDER" == "TRUE" ]] && ordered="true"
  case "$(printf '%s' "$GRAPHICAL_ALLOW_DUPLICATES" | tr '[:upper:]' '[:lower:]')" in
    1|true|yes|on) duplicates="true" ;;
  esac
  local cp
  cp="$(local_sequence_tool_classpath)"
  if [[ "$(id -un)" == "$IDP_RUN_USER" ]]; then
    env JAVA_HOME="$JAVA_HOME_VALUE" "$JAVA_HOME_VALUE/bin/java" -cp "$cp" \
      io.github.yasakawa.faskw.GraphicalMatrixSequenceTool \
      encode "$IDP_HOME" "$sequence" "$ordered" "$duplicates"
  else
    sudo -u "$IDP_RUN_USER" env JAVA_HOME="$JAVA_HOME_VALUE" "$JAVA_HOME_VALUE/bin/java" -cp "$cp" \
      io.github.yasakawa.faskw.GraphicalMatrixSequenceTool \
      encode "$IDP_HOME" "$sequence" "$ordered" "$duplicates"
  fi
}

ldap_entry_has_object_class() {
  local dn="$1"
  local object_class="$2"
  ldapsearch -x -H "$LDAP_STORAGE_URL" \
    -D "$LDAP_STORAGE_BIND_DN" -w "$(ldap_storage_bind_password)" \
    -b "$dn" -s base objectClass 2>/dev/null \
    | grep -Fq "objectClass: $object_class"
}

ldap_add_object_class_if_missing() {
  local dn="$1"
  local object_class="$2"
  [[ -n "$object_class" ]] || return 0
  say "+ ldapmodify objectClass if missing: $object_class on $dn"
  if ldap_entry_has_object_class "$dn" "$object_class"; then
    say "KEEP: existing LDAP objectClass: $object_class"
    return 0
  fi
  ldapmodify -x -H "$LDAP_STORAGE_URL" \
    -D "$LDAP_STORAGE_BIND_DN" -w "$(ldap_storage_bind_password)" <<EOF
dn: $dn
changetype: modify
add: objectClass
objectClass: $object_class
EOF
}

ldap_assert_graphical_attrs_present() {
  local dn="$1"
  local result
  say "+ verify GraphicalMatrix LDAP attributes for $dn"
  result="$(ldapsearch -LLL -x -H "$LDAP_STORAGE_URL" \
    -D "$LDAP_STORAGE_BIND_DN" -w "$(ldap_storage_bind_password)" \
    -b "$dn" -s base \
    objectClass "$LDAP_STORAGE_ATTR_SEQUENCE" "$LDAP_STORAGE_ATTR_STATUS" "$LDAP_STORAGE_ATTR_MFA_METHOD")"

  grep -Fq "objectClass: $LDAP_STORAGE_OBJECT_CLASS" <<< "$result" \
    || die "GraphicalMatrix LDAP objectClass was not provisioned on $dn"
  grep -Fq "$LDAP_STORAGE_ATTR_SEQUENCE:" <<< "$result" \
    || die "GraphicalMatrix LDAP sequence attribute was not provisioned on $dn"
  grep -Fq "$LDAP_STORAGE_ATTR_STATUS: ACTIVE" <<< "$result" \
    || die "GraphicalMatrix LDAP status attribute was not provisioned as ACTIVE on $dn"
  grep -Fq "$LDAP_STORAGE_ATTR_MFA_METHOD: $LOCAL_TEST_GRAPHICAL_METHOD" <<< "$result" \
    || die "GraphicalMatrix LDAP MFA method attribute was not provisioned on $dn"
}

