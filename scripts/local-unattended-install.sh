usage() {
  cat <<'EOF'
Usage:
  local-unattended-install.sh [--apply] [--config FILE] [--savedata db|ldap] [--webauthn] [--allow-existing] [--skip-build] [--skip-restart]

Default mode is dry-run. No IdP files are changed without --apply.

Options:
  --apply         Perform installation and configuration.
  --config FILE   Source an additional local shell configuration file.
  --savedata MODE Set GraphicalMatrix enrollment storage for this run: db or ldap.
                  This overrides GRAPHICAL_SAVEDATA from the script/config file.
  --webauthn      Enable WebAuthn test configuration. With --savedata ldap and
