        warn "build script was not found under $ROOT_DIR; using $INSTALL_MEDIA_DIR instead"
        if [[ -z "$PACKAGE_DIR" ]]; then
          PACKAGE_DIR="$(first_media_dir '2faskw-idp-plugin-*')"
        fi
        if [[ -z "$PACKAGE_DIR" ]]; then
          PACKAGE_ZIP="$(find_plugin_package_zip || true)"
          if [[ -z "$PACKAGE_ZIP" ]]; then
            if [[ "$APPLY" -eq 0 ]]; then
              warn "plugin package ZIP not found; dry-run will skip 2FAS-KW package installation/configuration steps"
              say "INFO: for --apply, place 2faskw-idp-plugin-*.zip under $INSTALL_MEDIA_DIR, $SCRIPT_DIR, or the current directory, or set PACKAGE_DIR"
              PACKAGE_DIR="<deferred-plugin-package>"
              PACKAGE_DEFERRED=1
              return 0
            fi
            die "plugin package ZIP not found; place 2faskw-idp-plugin-*.zip under $INSTALL_MEDIA_DIR, $SCRIPT_DIR, or the current directory, or set PACKAGE_DIR"
          fi
          require_command unzip
          WORK_DIR="$PACKAGE_EXTRACT_ROOT/$TS"
          mkdir -p "$WORK_DIR"
          unzip -q "$PACKAGE_ZIP" -d "$WORK_DIR"
          PACKAGE_DIR="$(find "$WORK_DIR" -maxdepth 1 -type d \
            -name '2faskw-idp-plugin-*' 2>/dev/null | sort -V | tail -n 1)"
        fi
      fi
      ;;
    directory)
      if [[ -z "$PACKAGE_DIR" ]]; then
        if [[ "$APPLY" -eq 0 ]]; then
          warn "PACKAGE_DIR is empty; dry-run will skip 2FAS-KW package installation/configuration steps"
          PACKAGE_DIR="<deferred-plugin-package>"
          PACKAGE_DEFERRED=1
          return 0
        fi
        die "PACKAGE_DIR is required for PACKAGE_SOURCE_MODE=directory"
      fi
      ;;
    zip)
      if [[ -z "$PACKAGE_ZIP" ]]; then
        PACKAGE_ZIP="$(find_plugin_package_zip || true)"
      fi
      if [[ -z "$PACKAGE_ZIP" || ! -f "$PACKAGE_ZIP" ]]; then
        if [[ "$APPLY" -eq 0 ]]; then
          warn "PACKAGE_ZIP is not available; dry-run will skip 2FAS-KW package installation/configuration steps"
          PACKAGE_DIR="<deferred-plugin-package>"
          PACKAGE_DEFERRED=1
          return 0
        fi
        die "PACKAGE_ZIP is required for PACKAGE_SOURCE_MODE=zip"
      fi
      require_command unzip
      if [[ -n "$PACKAGE_SHA256" ]]; then
        local actual
        if command -v sha256sum >/dev/null 2>&1; then
          actual="$(sha256sum "$PACKAGE_ZIP" | awk '{print $1}')"
        else
          actual="$(shasum -a 256 "$PACKAGE_ZIP" | awk '{print $1}')"
        fi
        [[ "$(printf '%s' "$actual" | tr '[:upper:]' '[:lower:]')" \
            == "$(printf '%s' "$PACKAGE_SHA256" | tr '[:upper:]' '[:lower:]')" ]] \
          || die "package SHA-256 mismatch: actual=$actual"
        say "OK: package SHA-256 verified"
      else
        warn "PACKAGE_SHA256 is empty; ZIP checksum verification was skipped"
      fi
      verify_zip_signature
      WORK_DIR="$PACKAGE_EXTRACT_ROOT/$TS"
      mkdir -p "$WORK_DIR"
      unzip -q "$PACKAGE_ZIP" -d "$WORK_DIR"
      PACKAGE_DIR="$(find "$WORK_DIR" -maxdepth 1 -type d \
        -name '2faskw-idp-plugin-*' 2>/dev/null | sort -V | tail -n 1)"
      ;;
    *)
      die "PACKAGE_SOURCE_MODE must be build, directory, or zip"
      ;;
  esac

  [[ -n "$PACKAGE_DIR" ]] || die "unable to determine PACKAGE_DIR"
  if [[ "$PACKAGE_DEFERRED" -eq 1 ]]; then
    return 0
  fi
  require_dir "$PACKAGE_DIR"
  require_file "$PACKAGE_DIR/bin/graphicalmatrix-plugin-check.sh"
  require_file "$PACKAGE_DIR/bin/graphicalmatrix-plugin-config.sh"
  require_file "$PACKAGE_DIR/bin/graphicalmatrix-plugin-webxml.sh"
}

