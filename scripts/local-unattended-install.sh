  --skip-build    Do not run the IdP WAR build step.
  --skip-restart  Do not restart Jetty.
  -h, --help      Show this help.

Recommended:
  1. Edit the configuration block at the beginning of this script.
  2. Run without --apply and review every command.
  3. Run with --apply in a new/test IdP environment.

Important:
  This is a release validation helper, not a production installer. It assumes a
  local LDAP directory and, when DB storage or WebAuthn is enabled, a PostgreSQL
  test database on 127.0.0.1. It may create/update local test LDAP entries,
  the test DB role, and the test DB specified in the configuration block.
  When enabled, it also builds a
  browser-reachable SimpleSAMLphp test SP on http://<test-host>:8081.

Local media:
  By default the script downloads missing local validation media under
  ~/install-media. You may also place the files there in advance:
    jetty-home-*.tar.gz
    shibboleth-identity-provider-*.tar.gz
    simplesamlphp-*-full.tar.gz
EOF
