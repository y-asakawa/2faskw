detect_local_test_public_host() {
  local detected first_ip local_ips resolved_ip
  local_ips="$(hostname -I 2>/dev/null | tr ' ' '\n' \
    | awk '$1 !~ /^127\./ && $1 !~ /^::1$/ {print}')"
  detected="$(hostname -f 2>/dev/null || true)"
  if [[ -n "$detected"
      && "$detected" != "localhost"
      && "$detected" != "localhost.localdomain"
      && "$detected" != *"(none)"* ]]; then
    while read -r resolved_ip _; do
      [[ -n "$resolved_ip" ]] || continue
      if printf '%s\n' "$local_ips" | grep -Fxq "$resolved_ip"; then
        printf '%s' "$detected"
        return 0
      fi
    done < <(getent ahosts "$detected" 2>/dev/null || true)
  fi
  first_ip="$(printf '%s\n' "$local_ips" | awk 'NF {print; exit}')"
  if [[ -n "$first_ip" ]]; then
    printf '%s' "$first_ip"
    return 0
  fi
  printf '127.0.0.1'
}

